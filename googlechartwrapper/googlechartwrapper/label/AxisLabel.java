package googlechartwrapper.label;

public class AxisLabel{
	
	protected String label;
	protected int pos;

	public AxisLabel(String label) {
		super();
		this.label = label;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public int getPos() {
		return pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}
	
	


}
